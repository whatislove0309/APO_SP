#include "button.h"

Button::Button(int x, int y, int width, int height) {
    this->x = x;
    this->y = y;
    this->width = width;
    this->height = height;
}

int Button::getX() {
    return x;
}
int Button::getY() {
    return x;
}
int Button::getWidth() {
    return x;
}
int Button::getHeight() {
    return x;
}
